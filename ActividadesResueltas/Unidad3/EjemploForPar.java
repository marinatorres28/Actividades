package Unidad3;

public class EjemploForPar {
    public static void main(String[] args) {
        //mostrar los numeros pares del 0 al 200
        for (int i=0; i <= 200; i+=2){ //sintaxis es i+=incremento que necesites
            System.out.println("El valor de i es-> " + i);
        }
    }
}
